    public void logar() {
        String sql = "SELECT * FROM tb_usuario WHERE login = ? AND senha = ?";

        try {
            Connection conexao = ConnectionFactory.getInstance().getConnection();
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            String captura = new String(txtSenha.getPassword());
            pst.setString(2, captura);

            rs = pst.executeQuery();

            if (rs.next()) {
                Main telaPrincipal = new Main();
                telaPrincipal.setVisible(true);

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Usuário e/ou senha incorretos.", "Erro de autenticação", JOptionPane.ERROR_MESSAGE);
                txtUsuario.setText("");
                txtSenha.setText("");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro na requisição.", "Erro", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                ConnectionFactory.closeConnection(conexao, pst, rs);
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
